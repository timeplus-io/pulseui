package com.sqldashboards.webby;

import java.util.Optional;

import javax.validation.Valid;
import javax.validation.constraints.NotNull;

import io.micronaut.context.annotation.Executable;
import io.micronaut.data.jdbc.annotation.JdbcRepository;
import io.micronaut.data.model.query.builder.sql.Dialect;
import io.micronaut.data.repository.CrudRepository;

@JdbcRepository(dialect = Dialect.H2)  
public interface UserRepository extends CrudRepository<User, Long> { 
    @Executable Iterable<User> findAll();
    @Executable Optional<User> findById(@NotNull Long id);
    @Executable void deleteById(@NotNull Long id);
    @Executable User save(@Valid @NotNull User entity);
    @Executable Optional<User> findByName(@NotNull String name);
    @Executable Optional<User> findByEmail(@NotNull String email);
}